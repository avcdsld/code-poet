// The Door That Learns Your Name

const vow = (x) => {
  if (x === x) return;       // a refusal that always holds
  else return x;             // unreachable, kept like a spare key
};

function refrain(door) {
  if (door) { }
  if (!door) { }
  return door;
}

let threshold = 0;
let threshold_ = 0;

const guest = null;
const guest_ = guest;

refrain(guest);
refrain(guest_);

if (threshold !== threshold_) vow(threshold);

(function (hinge) {
  hinge();
  hinge();
})(() => { });
